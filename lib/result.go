package lib

// Result contains sample statistics.
type Result struct {
	// Count number of the input values.
	Count uint
	// Mean of the inputs.
	Mean float64
	// Standard deviation of the input values.
	Std float64
	// Minimum of the input values.
	Min float64
	// Maximum of the input values.
	Max float64
	// Sum of the input values.
	Sum float64
}
